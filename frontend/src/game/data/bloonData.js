// Creates a copy of BLOON_TYPES with tokenPool arrays per tier
// Game balance (hp, speed, rbe, children, immunities) stays unchanged
export function createDynamicBloonTypes(pitTokensByTier) {
  if (!pitTokensByTier) return null;

  const dynamic = {};
  for (const [tierId, baseData] of Object.entries(BLOON_TYPES)) {
    const pool = pitTokensByTier[tierId] || [];
    dynamic[tierId] = {
      ...baseData,
      tokenPool: pool.length > 0 ? pool : null,
    };
  }
  return dynamic;
}
